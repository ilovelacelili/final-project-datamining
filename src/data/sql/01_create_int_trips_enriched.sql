--  Tabla intermedia INT_TRIPS_ENRICHED

CREATE OR REPLACE TABLE RAW.INT_TRIPS_ENRICHED AS

-- Yellow 
SELECT
    'yellow' AS SERVICE_TYPE,
    VENDORID AS VENDOR_ID,
    PICKUP_DATETIME,
    DROPOFF_DATETIME,
    PASSENGER_COUNT,
    TRIP_DISTANCE,
    RATECODEID AS RATE_CODE_ID,
    STORE_AND_FWD_FLAG,
    PULOCATIONID AS PU_LOCATION_ID,
    DOLOCATIONID AS DO_LOCATION_ID,
    PAYMENT_TYPE,
    FARE_AMOUNT,
    EXTRA,
    MTA_TAX,
    TIP_AMOUNT,
    TOLLS_AMOUNT,
    IMPROVEMENT_SURCHARGE,
    TOTAL_AMOUNT,
    CONGESTION_SURCHARGE,
    AIRPORT_FEE,
    NULL AS EHAIL_FEE,       
    NULL AS TRIP_TYPE,        
    NULL AS TRIP_TYPE_DESC,   
    -- Zonas de pickup y dropoff
    tz_pu.BOROUGH AS PU_BOROUGH,
    tz_pu.ZONE AS PU_ZONE,
    tz_do.BOROUGH AS DO_BOROUGH,
    tz_do.ZONE AS DO_ZONE,
    -- Catálogos descriptivos
    v.VENDOR_NAME,
    pt.PAYMENT_TYPE_DESC,
    rc.RATE_CODE_DESC,
    -- Linaje de carga
    RUN_ID,
    SOURCE_YEAR,
    SOURCE_MONTH,
    SOURCE_PATH,
    INGESTED_AT_UTC

FROM RAW.TRIPS_YELLOW

LEFT JOIN RAW.TAXI_ZONES tz_pu ON PULOCATIONID = tz_pu.LOCATIONID
LEFT JOIN RAW.TAXI_ZONES tz_do ON DOLOCATIONID = tz_do.LOCATIONID
LEFT JOIN RAW.DIM_VENDOR v ON VENDORID = v.VENDOR_ID
LEFT JOIN RAW.DIM_PAYMENT_TYPE pt ON PAYMENT_TYPE = pt.PAYMENT_TYPE_ID
LEFT JOIN RAW.DIM_RATE_CODE rc ON RATECODEID = rc.RATE_CODE_ID

UNION ALL

-- Green
SELECT
    'green' AS SERVICE_TYPE,
    VENDORID AS VENDOR_ID,
    PICKUP_DATETIME,
    DROPOFF_DATETIME,
    PASSENGER_COUNT,
    TRIP_DISTANCE,
    RATECODEID AS RATE_CODE_ID,
    STORE_AND_FWD_FLAG,
    PULOCATIONID AS PU_LOCATION_ID,
    DOLOCATIONID AS DO_LOCATION_ID,
    PAYMENT_TYPE,
    FARE_AMOUNT,
    EXTRA,
    MTA_TAX,
    TIP_AMOUNT,
    TOLLS_AMOUNT,
    IMPROVEMENT_SURCHARGE,
    TOTAL_AMOUNT,
    CONGESTION_SURCHARGE,
    NULL AS AIRPORT_FEE,      
    EHAIL_FEE,
    TRIP_TYPE,
    tt.TRIP_TYPE_DESC,
    -- Zonas de pickup y dropoff
    tz_pu.BOROUGH AS PU_BOROUGH,
    tz_pu.ZONE AS PU_ZONE,
    tz_do.BOROUGH AS DO_BOROUGH,
    tz_do.ZONE AS DO_ZONE,
    -- Catálogos descriptivos
    v.VENDOR_NAME,
    pt.PAYMENT_TYPE_DESC,
    rc.RATE_CODE_DESC,
    -- Linaje de carga
    RUN_ID,
    SOURCE_YEAR,
    SOURCE_MONTH,
    SOURCE_PATH,
    INGESTED_AT_UTC

FROM RAW.TRIPS_GREEN

LEFT JOIN RAW.TAXI_ZONES tz_pu ON PULOCATIONID = tz_pu.LOCATIONID
LEFT JOIN RAW.TAXI_ZONES tz_do ON DOLOCATIONID = tz_do.LOCATIONID
LEFT JOIN RAW.DIM_VENDOR v ON VENDORID = v.VENDOR_ID
LEFT JOIN RAW.DIM_PAYMENT_TYPE pt ON PAYMENT_TYPE = pt.PAYMENT_TYPE_ID
LEFT JOIN RAW.DIM_RATE_CODE rc ON RATECODEID = rc.RATE_CODE_ID
LEFT JOIN RAW.DIM_TRIP_TYPE tt ON CAST(TRIP_TYPE AS INTEGER) = tt.TRIP_TYPE_ID;

-- Verificación final

-- Total de filas 
SELECT COUNT(*) AS TOTAL_FILAS FROM RAW.INT_TRIPS_ENRICHED;

-- Filas por servicio y año
SELECT
    SERVICE_TYPE,
    SOURCE_YEAR,
    COUNT(*) AS VIAJES
FROM RAW.INT_TRIPS_ENRICHED
GROUP BY SERVICE_TYPE, SOURCE_YEAR
ORDER BY SERVICE_TYPE, SOURCE_YEAR;

-- Control de nulos en columnas clave
SELECT
    SUM(CASE WHEN PICKUP_DATETIME  IS NULL THEN 1 ELSE 0 END) AS NULL_PICKUP,
    SUM(CASE WHEN DROPOFF_DATETIME IS NULL THEN 1 ELSE 0 END) AS NULL_DROPOFF,
    SUM(CASE WHEN PU_LOCATION_ID IS NULL THEN 1 ELSE 0 END) AS NULL_PU,
    SUM(CASE WHEN DO_LOCATION_ID IS NULL THEN 1 ELSE 0 END) AS NULL_DO,
    SUM(CASE WHEN PU_ZONE IS NULL THEN 1 ELSE 0 END) AS NULL_PU_ZONE,
    SUM(CASE WHEN VENDOR_NAME IS NULL THEN 1 ELSE 0 END) AS NULL_VENDOR
FROM RAW.INT_TRIPS_ENRICHED;
