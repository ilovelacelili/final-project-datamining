-- 02_create_obt_trips.sql

-- Crear la OBT 

CREATE OR REPLACE TABLE ANALYTICS.OBT_TRIPS AS

WITH base AS (

    SELECT
        SERVICE_TYPE,
        VENDOR_ID,
        VENDOR_NAME,
        PICKUP_DATETIME,
        DROPOFF_DATETIME,

        -- Columnas temporales derivadas 
        DATE(PICKUP_DATETIME) AS PICKUP_DATE,
        HOUR(PICKUP_DATETIME) AS PICKUP_HOUR,
        DAYOFWEEK(PICKUP_DATETIME) AS DAY_OF_WEEK,
        MONTH(PICKUP_DATETIME) AS MONTH,
        YEAR(PICKUP_DATETIME) AS YEAR,

        -- Columnas temporales del dropoff 
        DATE(DROPOFF_DATETIME) AS DROPOFF_DATE,
        HOUR(DROPOFF_DATETIME) AS DROPOFF_HOUR,

        -- Zonas 
        PU_LOCATION_ID,
        PU_ZONE,
        PU_BOROUGH,
        DO_LOCATION_ID,
        DO_ZONE,
        DO_BOROUGH,

        -- Atributos del viaje 
        RATE_CODE_ID,
        RATE_CODE_DESC,
        PAYMENT_TYPE,
        PAYMENT_TYPE_DESC,
        TRIP_TYPE,          
        PASSENGER_COUNT,
        TRIP_DISTANCE,
        STORE_AND_FWD_FLAG,

        -- Componentes tarifarios 
        FARE_AMOUNT,
        EXTRA,
        MTA_TAX,
        TIP_AMOUNT,
        TOLLS_AMOUNT,
        IMPROVEMENT_SURCHARGE,
        CONGESTION_SURCHARGE,
        AIRPORT_FEE,
        EHAIL_FEE,
        TOTAL_AMOUNT,      

        -- Derivadas de viaje 
        DATEDIFF('second', PICKUP_DATETIME, DROPOFF_DATETIME) / 60.0 AS TRIP_DURATION_MIN,

        CASE
            WHEN DATEDIFF('second', PICKUP_DATETIME, DROPOFF_DATETIME) > 0 AND TRIP_DISTANCE > 0
            THEN TRIP_DISTANCE / (DATEDIFF('second', PICKUP_DATETIME, DROPOFF_DATETIME) / 3600.0)
            ELSE NULL
        END AS AVG_SPEED_MPH,

        CASE
            WHEN TOTAL_AMOUNT > 0
            THEN (TIP_AMOUNT / TOTAL_AMOUNT) * 100.0
            ELSE NULL
        END AS TIP_PCT,

        -- Linaje 
        RUN_ID,
        SOURCE_YEAR,
        SOURCE_MONTH,
        SOURCE_PATH,
        INGESTED_AT_UTC

    FROM RAW.INT_TRIPS_ENRICHED

)

SELECT *
FROM base

-- Limpieza  

-- Nulos en columnas obligatorias
WHERE PICKUP_DATETIME IS NOT NULL
  AND DROPOFF_DATETIME IS NOT NULL
  AND PU_LOCATION_ID IS NOT NULL
  AND DO_LOCATION_ID IS NOT NULL
  AND SERVICE_TYPE IS NOT NULL
  AND TOTAL_AMOUNT IS NOT NULL
  AND PASSENGER_COUNT IS NOT NULL
  AND TRIP_DISTANCE IS NOT NULL

-- Timestamps válidos (pickup no puede ser posterior al dropoff)
  AND PICKUP_DATETIME <= DROPOFF_DATETIME

-- Rangos lógicos
  AND (TRIP_DISTANCE >= 0 OR TRIP_DISTANCE IS NULL)
  AND (TOTAL_AMOUNT >= 0 OR TOTAL_AMOUNT IS NULL)
  AND (PASSENGER_COUNT BETWEEN 0 AND 9 OR PASSENGER_COUNT IS NULL)
  AND (TRIP_DURATION_MIN BETWEEN 0 AND 1440 OR TRIP_DURATION_MIN IS NULL)
  AND (AVG_SPEED_MPH BETWEEN 0 AND 120 OR AVG_SPEED_MPH IS NULL)
  AND (TIP_PCT BETWEEN 0 AND 100 OR TIP_PCT IS NULL)

-- Consistencia interna
  AND (FARE_AMOUNT <= TOTAL_AMOUNT OR FARE_AMOUNT IS NULL OR TOTAL_AMOUNT IS NULL)
  -- Green debe tener TRIP_TYPE siempre
  AND NOT (SERVICE_TYPE = 'green' AND TRIP_TYPE IS NULL)
  -- El año del pickup debe coincidir con el año de la fuente 
  AND (YEAR(PICKUP_DATETIME) = SOURCE_YEAR::INT OR PICKUP_DATETIME IS NULL OR SOURCE_YEAR IS NULL)
  AND (MONTH(PICKUP_DATETIME) = SOURCE_MONTH::INT OR PICKUP_DATETIME IS NULL OR SOURCE_MONTH IS NULL);


-- Verificación final

-- Columnas y total de filas 
SELECT COUNT(*) AS TOTAL_FILAS FROM ANALYTICS.OBT_TRIPS;

-- Distribución por servicio y año
SELECT
    SERVICE_TYPE,
    SOURCE_YEAR,
    COUNT(*) AS VIAJES,
    ROUND(AVG(FARE_AMOUNT), 2) AS AVG_FARE,
    ROUND(AVG(TRIP_DISTANCE), 2) AS AVG_DIST_MI,
    ROUND(AVG(TRIP_DURATION_MIN), 2) AS AVG_DUR_MIN
FROM ANALYTICS.OBT_TRIPS
GROUP BY SERVICE_TYPE, SOURCE_YEAR
ORDER BY SERVICE_TYPE, SOURCE_YEAR;
