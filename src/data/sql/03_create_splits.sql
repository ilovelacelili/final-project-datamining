-- 03_create_splits.sql

-- TRAIN SET: 2015 – 2023
CREATE OR REPLACE TABLE ANALYTICS.TRAIN_SET AS
SELECT *
FROM ANALYTICS.OBT_TRIPS
WHERE YEAR = 2015
   OR YEAR = 2016
   OR YEAR = 2017
   OR YEAR = 2018
   OR YEAR = 2019
   OR YEAR = 2020
   OR YEAR = 2021
   OR YEAR = 2022
   OR YEAR = 2023;

-- VALIDATION SET: 2024
CREATE OR REPLACE TABLE ANALYTICS.VAL_SET AS
SELECT *
FROM ANALYTICS.OBT_TRIPS
WHERE YEAR = 2024;

-- TEST SET: 2025
CREATE OR REPLACE TABLE ANALYTICS.TEST_SET AS
SELECT *
FROM ANALYTICS.OBT_TRIPS
WHERE YEAR = 2025;

-- Verificación: conteos y cobertura temporal de cada split

-- Tamaños de cada split
SELECT 'TRAIN' AS SPLIT, COUNT(*) AS FILAS FROM ANALYTICS.TRAIN_SET
UNION ALL
SELECT 'VAL', COUNT(*) FROM ANALYTICS.VAL_SET
UNION ALL
SELECT 'TEST', COUNT(*) FROM ANALYTICS.TEST_SET;

-- Verificar que no hay solapamiento entre splits
SELECT 'TRAIN_en_VAL' AS CHECK_NOMBRE, COUNT(*) AS FILAS_SOLAPADAS
FROM ANALYTICS.TRAIN_SET WHERE YEAR >= 2024
UNION ALL
SELECT 'TRAIN_en_TEST', COUNT(*)
FROM ANALYTICS.TRAIN_SET WHERE YEAR >= 2025
UNION ALL
SELECT 'VAL_en_TEST', COUNT(*)
FROM ANALYTICS.VAL_SET WHERE YEAR >= 2025;

-- Distribución por año dentro de cada split 
SELECT
    'TRAIN'  AS SPLIT,
    YEAR,
    SERVICE_TYPE,
    COUNT(*) AS VIAJES
FROM ANALYTICS.TRAIN_SET
GROUP BY YEAR, SERVICE_TYPE

UNION ALL

SELECT 'VAL', YEAR, SERVICE_TYPE, COUNT(*)
FROM ANALYTICS.VAL_SET
GROUP BY YEAR, SERVICE_TYPE

UNION ALL

SELECT 'TEST', YEAR, SERVICE_TYPE, COUNT(*)
FROM ANALYTICS.TEST_SET
GROUP BY YEAR, SERVICE_TYPE

ORDER BY SPLIT, YEAR, SERVICE_TYPE;
